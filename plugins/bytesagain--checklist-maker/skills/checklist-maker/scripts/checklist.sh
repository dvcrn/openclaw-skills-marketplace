#!/usr/bin/env bash
# checklist.sh — 清单生成器
# Usage: bash checklist.sh <command> [args...]

set -euo pipefail

CMD="${1:-help}"
shift 2>/dev/null || true

show_help() {
  cat <<'HELP'
Checklist Maker ✅ — 清单生成器

用法: bash checklist.sh <command> [args...]

命令:
  create       自定义主题清单生成
  travel       旅行打包/准备清单
  project      项目启动/上线清单
  daily        每日待办/习惯清单
  moving       搬家全流程清单
  emergency    应急准备/急救清单

示例:
  bash checklist.sh travel
  bash checklist.sh daily
  bash checklist.sh project

  Powered by BytesAgain | bytesagain.com | hello@bytesagain.com
HELP
}

cmd_create() {
  cat <<'EOF'
## 📋 自定义清单生成器

请AI根据用户需求生成Markdown清单：

### 输入信息
- **清单主题**: [用户提供]
- **使用场景**: [用户提供]
- **详细程度**: [简洁/标准/详细]

### 输出格式
```markdown
# [清单主题] 清单

> 创建时间: [日期]
> 总计: X项

## 📌 优先级 P0 (必做)
- [ ] [条目1]
- [ ] [条目2]

## 📋 优先级 P1 (应做)
- [ ] [条目3]
- [ ] [条目4]

## 💡 优先级 P2 (可做)
- [ ] [条目5]
- [ ] [条目6]

---
✅ 完成: 0/X | 📅 截止: [日期]
```

### 清单设计原则
1. 每个条目用动词开头（买/准备/检查/确认）
2. 条目足够具体，看到就能执行
3. 按逻辑分组（时间/类别/优先级）
4. 标注预估时间（可选）
5. 总数控制在50项以内
EOF
}


## 🧊 破冰游戏推荐

### 快速破冰 (5-10分钟)

**1. 两真一假**
- 规则：每人说3个关于自己的事实，其中1个是假的，其他人猜
- 人数：5-20人
- 效果：快速了解彼此

**2. 人体BINGO**
- 规则：制作BINGO卡片（如"去过5个国家""会弹吉他"），找人签名
- 人数：10-50人
- 效果：主动社交

**3. 快速问答接龙**
- 规则：限时1分钟回答趣味问题，传递给下一个人
- 人数：8-30人
- 效果：活跃气氛

### 中度互动 (15-30分钟)

**4. 团队拼图挑战**
- 规则：分组完成拼图，但每组缺少几块在别组手中
- 人数：12-40人
- 效果：跨组协作

**5. 画像猜人**
- 规则：匿名写3个个人特点，随机分配，根据描述画像并猜是谁
- 人数：8-25人
- 效果：有趣互动

**6. 故事接龙**
- 规则：给定开头，每人续写一句，最终念出完整故事
- 人数：6-20人
- 效果：创意表达

### 深度融合 (30-60分钟)

**7. 人生地图**
- 规则：画出自己人生重要节点的地图并分享
- 人数：5-15人
- 效果：深度了解

**8. 价值观排序**
- 规则：给定20个价值观卡片，个人排序后小组讨论达成共识
- 人数：8-24人
- 效果：价值观对齐

**9. 荒岛求生**
- 规则：假设流落荒岛，从15件物品中团队协商选5件
- 人数：6-20人
- 效果：决策协作

### 适用场景选择
| 场景 | 推荐游戏 | 时长 |
|------|----------|------|
| 新员工入职 | 两真一假 + 人体BINGO | 20min |
| 跨部门协作 | 团队拼图 + 价值观排序 | 45min |
| 年会/大型活动 | 人体BINGO + 故事接龙 | 30min |
| 会议开场 | 快速问答接龙 | 10min |
| 管理层共识 | 人生地图 + 价值观排序 | 60min |

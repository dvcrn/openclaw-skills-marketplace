cmd_cpr() {
cat << 'EOF'
❤️ 心肺复苏 (CPR) 步骤

适用：意识丧失、无呼吸或仅有濒死呼吸的成人

【确认安全】
1. 确认现场安全，戴手套（如有）
2. 轻拍双肩，大声呼叫"你还好吗？"
3. 无反应 → 立即拨打120（让旁人打）
4. 让人去取AED（自动体外除颤器）

【30次胸外按压】
- 位置：两乳头连线中点（胸骨中下1/3）
- 姿势：跪在患者一侧，双手掌根重叠
- 深度：至少5cm，不超过6cm
- 速率：100-120次/分钟（跟着"Stayin' Alive"节奏）
- 要点：每次按压后让胸廓完全回弹
- ⚠️ 不要怕按断肋骨，救命优先！

【2次人工呼吸】
- 仰头抬颏，打开气道
- 捏住鼻子，口对口吹气1秒
- 观察胸廓是否起伏
- 如不愿口对口，可只做胸外按压

【循环】
- 30次按压 : 2次呼吸 = 1个循环
- 持续进行直到：
  ① 120到达
  ② AED到位
  ③ 患者恢复呼吸
  ④ 你体力耗尽（换人继续）

【AED使用】
1. 开机，听语音提示
2. 贴电极片（右锁骨下+左腋下）
3. 分析心律时不要触碰患者
4. 提示电击时确保所有人离开
5. 电击后立即继续CPR

⚠️ 关键：按压不要停！
   每中断10秒，存活率下降7-10%
EOF
}

cmd_choking() {
cat << 'EOF'
🫁 噎住/异物梗阻 — 海姆立克急救法

【判断】
- 能咳嗽、能说话 → 鼓励用力咳嗽，不要拍背
- 不能咳嗽、不能说话、脸色发紫 → 立即施救

【成人/儿童(>1岁) 海姆立克法】
1. 站在患者身后
2. 一只脚插在患者两腿之间（防摔倒）
3. 找到肚脐位置
4. 一手握拳，拳眼对准肚脐上方两横指处
5. 另一手包住拳头
6. 快速向内向上冲击（J形）
7. 反复冲击直到异物排出

【婴儿(<1岁)】
1. 面朝下放在前臂上，头低于躯干
2. 用掌根在两肩胛骨之间拍背5次
3. 翻转面朝上，两指按压胸骨中点5次
4. 交替进行直到异物排出

【孕妇/肥胖者】
- 冲击位置改为胸骨中部（不能压腹部）

【自救（独自噎住）】
- 拳头抵住肚脐上方
- 靠在椅背/桌角上用力冲击

⚠️ 异物排出后仍建议就医检查
EOF
}

cmd_burn() {
cat << 'EOF'
🔥 烧伤/烫伤处理

【分度判断】
I度（表皮）：红肿疼痛，无水泡 → 可自行处理
II度（真皮）：水泡、剧痛 → 需就医
III度（全层）：焦黑/苍白、无痛感 → 立即急救

【急救步骤 — "冲脱泡盖送"】
1. 冲：流动冷水冲洗15-20分钟（不要冰水！）
2. 脱：冷水中小心脱去衣物（粘连的不要硬撕）
3. 泡：继续冷水浸泡15-30分钟
4. 盖：无菌纱布或干净布料轻轻覆盖
5. 送：II度以上或面积>手掌大小，立即就医

【禁忌】
❌ 不要涂牙膏、酱油、蜂蜜、黄油
❌ 不要挑破水泡
❌ 不要用冰块直接敷
❌ 不要撕掉粘在伤口上的衣物
❌ 不要用棉花覆盖（会粘连）

【化学烧伤】
- 大量清水持续冲洗至少20分钟
- 干粉化学物先刷掉再冲水
- 眼睛烧伤：头偏向受伤侧冲洗

⚠️ 烧伤面积超过体表10%或涉及面部/手/关节 → 120急救
EOF
}

cmd_bleeding() {
cat << 'EOF'
🩸 出血止血

【外出血】
1. 直接压迫法（首选）
   - 用干净纱布/布料直接按压伤口
   - 持续按压至少10-15分钟
   - 不要反复掀开看
   - 血液浸透后在上面加纱布继续按

2. 加压包扎
   - 直接压迫止血后，用绷带加压包扎
   - 检查远端脉搏确保不会太紧

3. 抬高伤肢（配合压迫）
   - 伤肢抬高于心脏水平

4. 止血带（最后手段，四肢大出血）
   - 使用在伤口近心端5-7cm处
   - 记录上止血带的时间！
   - 每1-2小时松开检查（60秒内）

【鼻出血】
- 头略前倾（不要后仰！会咽血）
- 捏住鼻翼（不是鼻梁）10-15分钟
- 可用冰袋敷鼻根

⚠️ 以下情况立即拨打120：
   - 喷射状出血（动脉破裂）
   - 压迫15分钟仍不止血
   - 伤口很深或有异物嵌入
EOF
}

cmd_heatstroke() {
cat << 'EOF'
☀️ 中暑急救

【判断程度】
先兆中暑：头晕、口渴、多汗 → 休息+补水
轻度中暑：头痛、恶心、体温>38℃ → 积极降温
重度中暑（热射病）：体温>40℃、意识障碍、无汗 → 致命！立即急救

【急救步骤】
1. 转移到阴凉通风处
2. 解开衣物、平躺
3. 积极降温：
   - 冷水擦拭全身（重点：腋下、腹股沟、颈部）
   - 冰袋敷大血管处
   - 扇风
4. 意识清醒：少量多次喝淡盐水
5. 意识不清：侧卧位防呕吐窒息

⚠️ 体温>40℃或意识不清 → 立即拨打120！
   热射病死亡率极高，争分夺秒！

【预防】
- 避免高温时段(11:00-15:00)户外活动
- 补充水和电解质
- 穿浅色透气衣物
EOF
}

cmd_fracture() {
cat << 'EOF'
🦴 骨折处理

【判断】
可能骨折的表现：
- 剧烈疼痛、肿胀
- 畸形（肢体弯曲异常）
- 功能障碍（不能活动）
- 骨摩擦音
- 反常活动

【急救原则：固定、不动、送医】
1. 不要试图复位！
2. 就地固定（用夹板/木板/杂志/伞）
   - 固定骨折上下两个关节
   - 在夹板和皮肤间垫软物
3. 开放性骨折（骨头外露）
   - 无菌敷料覆盖伤口
   - 不要把骨头推回去！
4. 止血 → 固定 → 送医

【脊柱/颈椎骨折 ⚠️最危险】
- 不要搬动！不要翻身！不要弯曲！
- 拨打120等专业急救
- 如必须搬运：至少3人，保持脊柱一直线
- 硬板搬运，颈部两侧用沙袋固定

⚠️ 怀疑脊柱损伤时，错误搬运可能导致终身瘫痪
EOF
}

cmd_poisoning() {
cat << 'EOF'
☠️ 中毒急救

【通用原则】
1. 确保自身安全（通风、防二次中毒）
2. 拨打120
3. 保留中毒物品/包装带去医院

【食物中毒】
- 意识清醒：可催吐（喝温水后刺激咽后壁）
- 意识不清：不要催吐！侧卧防窒息
- 腐蚀性物质(强酸/碱)：禁止催吐！

【气体中毒(CO等)】
1. 打开门窗通风
2. 将患者转移到新鲜空气处
3. 呼吸停止 → CPR
4. ⚠️ 不要开灯/打火机（防爆炸）

【药物过量】
- 收集药物包装和剩余药物
- 记录服药时间和数量
- 拨打120或中毒控制中心

【皮肤接触毒物】
- 脱去污染衣物
- 大量清水冲洗至少15-20分钟

⚠️ 中毒控制热线：120
EOF
}

cmd_drowning() {
cat << 'EOF'
🌊 溺水急救

【施救原则】
⚠️ 不会游泳不要下水救人！
优先顺序：叫(呼救) → 抛(救生圈/绳子) → 划(用船/板) → 游(最后手段)

【上岸后急救】
1. 清除口鼻异物（泥沙等）
2. 不需要控水！不要倒挂/压腹控水（浪费时间）
3. 检查呼吸：
   - 有呼吸 → 侧卧位，保暖，等120
   - 无呼吸 → 立即CPR
4. CPR要点：
   - 先做5次人工呼吸（溺水首要问题是缺氧）
   - 然后30:2正常CPR循环
5. 持续到120到达

⚠️ 溺水后即使看起来恢复正常
   也必须去医院检查（迟发性肺水肿）
EOF
}

cmd_shock() {
cat << 'EOF'
💫 休克处理

【识别休克】
- 面色苍白、皮肤湿冷
- 脉搏快而弱
- 呼吸急促
- 意识模糊或烦躁
- 血压下降

【急救步骤】
1. 拨打120
2. 平躺，抬高下肢15-30cm（增加回心血量）
   ⚠️ 头部/胸部受伤除外
3. 保暖（盖毯子）
4. 不要给食物或饮水
5. 松解紧身衣物
6. 有出血先止血
7. 持续观察意识和呼吸
8. 意识丧失 → 侧卧位
9. 呼吸停止 → CPR

【过敏性休克(严重过敏)】
- 如有肾上腺素笔(EpiPen)立即使用
- 注射部位：大腿外侧中部
- 注射后仍需拨打120
EOF
}


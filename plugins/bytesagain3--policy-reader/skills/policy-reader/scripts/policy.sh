def show_help():
    print("""
📜 政策解读助手
==============================

用法：policy.sh <命令> [参数...]

命令：
  summary <政策名称>              要点提炼
  impact  <政策名称> <行业>       影响分析
  subsidy <行业> <地区>           补贴政策梳理
  help                            显示此帮助

示例：
  policy.sh summary "关于促进数字经济发展的若干意见"
  policy.sh impact "数据安全法" "互联网"
  policy.sh subsidy "新能源" "深圳"
""")

if __name__ == "__main__":
    action = sys.argv[1] if len(sys.argv) > 1 else "help"
    args = sys.argv[2:]

    if action == "summary":
        if len(args) < 1:
            print("❌ 用法: policy.sh summary <政策名称>")
            sys.exit(1)
        summary(args[0])
    elif action == "impact":
        if len(args) < 2:
            print("❌ 用法: policy.sh impact <政策名称> <行业>")
            sys.exit(1)
        impact(args[0], args[1])
    elif action == "subsidy":
        if len(args) < 2:
            print("❌ 用法: policy.sh subsidy <行业> <地区>")
            sys.exit(1)
        subsidy(args[0], args[1])
    elif action == "help":
        show_help()
    else:
        print("❌ 未知命令: {}".format(action))
        show_help()
        sys.exit(1)
